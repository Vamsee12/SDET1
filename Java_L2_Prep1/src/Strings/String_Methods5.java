package Strings;

public class String_Methods5 {

	public static void main(String[] args) {
		
		String s1="Vamsee Mohan";
		
		System.out.println(s1.indexOf('n'));
		
		System.out.println(s1.lastIndexOf('e'));

		System.out.println(s1.lastIndexOf("an"));

		System.out.println(s1.charAt(3));
		
		System.out.println(s1.contains("v"));

		System.out.println(s1.contains("oh"));
		
		System.out.println(s1.startsWith("v"));
		
		System.out.println(s1.endsWith("n"));
		
	}

}
